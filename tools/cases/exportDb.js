/*
 * Dumps live case_sim ContentItems from MongoDB to tools/cases/db-existing.js so
 * the CLI generator can consume them via --existing.
 *
 * Usage:
 *   node tools/cases/exportDb.js
 *
 * Output: tools/cases/db-existing.js  (module.exports = { CASES: [...] })
 *
 * Cases whose id appears in SEED_CASES or GENERATED_CASES are excluded — they
 * are already known locally and don't need to be round-tripped.
 */

require('dotenv').config();
const path = require('path');
const fs = require('fs');
const mongoose = require('mongoose');

const { SEED_CASES } = require('./seed-cases');
const ContentItem = require('../../models/ContentItem');

// Build exclusion set from seed cases.
const excludedIds = new Set(SEED_CASES.map((c) => c.id));

// Also exclude anything already in generated-cases.js (it may not exist yet).
try {
  const { GENERATED_CASES } = require('./generated-cases');
  if (Array.isArray(GENERATED_CASES)) {
    GENERATED_CASES.forEach((c) => excludedIds.add(c.id));
  }
} catch (_) {
  // generated-cases.js doesn't exist yet — that's fine.
}

async function main() {
  if (!process.env.MONGO_URI) {
    console.error('MONGO_URI is not set — add it to your .env first.');
    process.exit(1);
  }

  await mongoose.connect(process.env.MONGO_URI, { dbName: 'passreadyprep' });
  console.log('Connected to MongoDB.');

  const docs = await ContentItem.find({ format: 'case_sim' }).lean();

  const cases = [];
  let excludedCount = 0;

  for (const doc of docs) {
    const caseSim = doc.caseSim || {};
    const id = caseSim.id || doc.externalId;

    if (excludedIds.has(id)) {
      excludedCount++;
      continue;
    }

    cases.push({
      ...caseSim,
      id,
      category: doc.category || caseSim.category,
    });
  }

  const outPath = path.join(__dirname, 'db-existing.js');
  const content =
    '// Auto-generated by exportDb.js — do not edit by hand.\n' +
    'module.exports = {\n' +
    '  CASES: ' +
    JSON.stringify(cases, null, 2) +
    ',\n};\n';

  fs.writeFileSync(outPath, content, 'utf8');

  console.log('Exported ' + cases.length + ' cases to db-existing.js');
  console.log('Excluded ' + excludedCount + ' ids (already in SEED_CASES / GENERATED_CASES).');

  await mongoose.disconnect();
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
